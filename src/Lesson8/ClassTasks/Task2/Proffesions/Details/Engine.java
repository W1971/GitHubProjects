package Lesson8.ClassTasks.Task2.Proffesions.Details;

public class Engine {
  private String power;
  private String maker;

  public Engine(String power, String maker) {
    this.power = power;
    this.maker = maker;
  }

  public String getPower() {
    return power;
  }

  public String getMaker() {
    return maker;
  }
}
